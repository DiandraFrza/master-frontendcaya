export const getBlogById = (id) => {
  return blogPosts.find((post) => post.id === Number(id));
};

export const getBlogsByCategory = (categoryId) => {
  if (categoryId === "all") return blogPosts;
  return blogPosts.filter((post) => post.category === categoryId);
};

export const getBlogsByTag = (tag) => {
  return blogPosts.filter((post) => post.tags.includes(tag));
};

export const getRelatedPosts = (currentId, category, limit = 3) => {
  return blogPosts
    .filter((post) => post.id !== Number(currentId) && post.category === category)
    .slice(0, limit);
};

export const searchBlogs = (query) => {
  const lowerQuery = query.toLowerCase();
  return blogPosts.filter(
    (post) =>
      post.title.toLowerCase().includes(lowerQuery) ||
      post.excerpt.toLowerCase().includes(lowerQuery) ||
      post.tags.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
};
